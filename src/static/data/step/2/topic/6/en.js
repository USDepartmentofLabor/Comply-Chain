            <p>
                The achievement of broad, shared goals&mdash;to prepare workers
                for good jobs, ensure fair compensation and achieve supply
                chains free of labor abuse&mdash;will continue to be hindered if
                efforts are not made to address the root causes of child labor
                and forced labor.
            </p>
